package main

import "fmt"

func main() {
	age := 20
	name := "amith"
	height := 5.9
	fmt.Println("age", age, "height", height, "name", name)
	fmt.Println("hello")
	fmt.Printf("age is %T\n", age)
	fmt.Printf("height is %f\n", height)
	//printf is format
	fmt.Printf("name is %s\n", name)
	fmt.Printf("hello world")
}
